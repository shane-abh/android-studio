import android.widget.ImageView;
import android.widget.TextView;
import android.widget.Toast;

import com.bumptech.glide.Glide;
import com.example.mainproject.ExpenseManager.AddFinancials;
import com.example.mainproject.ExpenseManager.ExpenseCalculatorMain;
import com.example.mainproject.StockPlatform.Search;
import com.google.android.gms.auth.api.Auth;
